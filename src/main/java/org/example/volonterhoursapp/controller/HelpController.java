    @FXML
    private void initialize() {
        Role role = CuratorSession.getRole();
        List<String> visible = new ArrayList<>();
        for (Map.Entry<String, Topic> e : TOPICS.entrySet()) {
            if (isAllowed(e.getValue().section(), role)) {
                visible.add(e.getKey());
            }
        }
        topicList.setItems(FXCollections.observableArrayList(visible));
        topicList.getSelectionModel().selectedItemProperty()
                .addListener((o, a, b) -> render(b));
        topicList.getSelectionModel().selectFirst();
    }

    /**
     * Виден ли раздел текущей роли. Повторяет логику доступа из {@code MainController}:
     * общие темы ({@code section == null}) видны всем, «Пользователи» — только админу,
     * админ и куратор видят всё, обычный пользователь — только {@link #USER_SECTIONS}.
     */
    private static boolean isAllowed(String section, Role role) {
        if (section == null) return true;
        if ("users".equals(section)) return role == Role.ADMIN;
        if (role == Role.ADMIN || role == Role.CURATOR) return true;
        return USER_SECTIONS.contains(section);
    }

    private void render(String topic) {
        content.getChildren().clear();
        if (topic == null) return;

        Label heading = new Label(topic);
        heading.getStyleClass().add("title-label");
        content.getChildren().add(heading);

        for (String line : TOPICS.get(topic).lines()) {
            content.getChildren().add(blockFor(line));
        }
    }

    private Region blockFor(String line) {
        if (line.startsWith("## ")) {
            Label h = new Label(line.substring(3));
            h.getStyleClass().add("section-header");
            VBox.setMargin(h, new Insets(10, 0, 0, 0));
            return h;
        }
        if (line.startsWith("• ")) {
            Label b = new Label(line);
            b.getStyleClass().add("help-bullet");
            b.setWrapText(true);
            VBox.setMargin(b, new Insets(0, 0, 0, 8));
            return b;
        }
        Label p = new Label(line);
        p.getStyleClass().add("help-paragraph");
        p.setWrapText(true);
        return p;
    }
